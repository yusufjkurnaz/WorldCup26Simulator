export const teamSquads = {};

// 1. Önce gerçek kadroları sisteme yüklüyoruz
Object.keys(kadrolar).forEach(ulke => {
    teamSquads[ulke] = kadrolar[ulke];
});

// Mantıklı mevkiler oluşturmak için bir havuz
const mevkiListesi = ["KL", "KL", "STP", "STP", "STP", "STP", "SĞB", "SĞB", "SLB", "SLB", "OS", "OS", "OS", "OS", "OS", "OS", "SĞK", "SĞK", "SLK", "SLK", "SNT", "SNT", "SNT", "STP", "OS", "SNT"];

// 2. Eksik kalan takımları oyunu çökertmemesi için geçici olarak dolduruyoruz
takimlar.forEach(takim => {
    if (!teamSquads[takim.isim]) {
        let uretilenKadro = [];
        let takimKodu = takim.isim.substring(0, 3).toUpperCase(); 
        
        let ortalamaGuc = Math.floor((takim.hucum + takim.ortaSaha + takim.savunma) / 3);

        for (let i = 0; i < 26; i++) {
            let numara = i + 1;
            let rastgeleSapma = Math.floor(Math.random() * 9) - 4; 
            let oyuncuGuc = ortalamaGuc + rastgeleSapma;
            let mevki = mevkiListesi[i];

            uretilenKadro.push({
                id: `${takimKodu}${String(numara).padStart(2, '0')}`,
                ad: `Oyuncu`, 
                soyad: `${numara}`,
                yas: Math.floor(Math.random() * 15) + 20, 
                mevki: mevki,
                gen: oyuncuGuc,
                formaNo: numara
            });
        }
        
        uretilenKadro.sort((a, b) => b.gen - a.gen);
        teamSquads[takim.isim] = uretilenKadro;
    }
});